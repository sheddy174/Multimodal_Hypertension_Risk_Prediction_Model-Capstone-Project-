class RetinaHypertensionPredictor:
    def __init__(self, model_path=None):
        """
        Initialize the retina fundus hypertension predictor
        """
        self.device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
        print(f"Using device: {self.device}")

        # Define the model architecture (same as during training)
        self.model = self._create_model()

        # Load trained weights if provided
        if model_path and os.path.exists(model_path):
            self.load_model(model_path)
        else:
            print("Warning: Model path not provided or not found. Using randomly initialized model.")

        # Image transformations (must match training)
        self.transform = transforms.Compose([
            transforms.Resize((224, 224)),
            transforms.ToTensor(),
            transforms.Normalize(mean=[0.485, 0.456, 0.406],
                               std=[0.229, 0.224, 0.225])
        ])

    def _create_model(self):
        """Create model with same architecture as training"""
        class FundusHypertensionModel(nn.Module):
            def __init__(self):
                super().__init__()
                # Using ResNet50 as backbone (same as training)
                self.backbone = models.resnet50(pretrained=False)
                in_features = self.backbone.fc.in_features
                self.backbone.fc = nn.Identity()

                # Feature extractor
                self.feature_extractor = nn.Sequential(
                    nn.Linear(in_features, 512),
                    nn.BatchNorm1d(512),
                    nn.ReLU(),
                    nn.Dropout(0.5),
                    nn.Linear(512, 256),
                    nn.BatchNorm1d(256),
                    nn.ReLU(),
                    nn.Dropout(0.4)
                )

                # Classifier
                self.classifier = nn.Linear(256, 1)

            def forward(self, x):
                x = self.backbone(x)
                features = self.feature_extractor(x)
                logits = self.classifier(features)
                return logits

        return FundusHypertensionModel().to(self.device)

    def load_model(self, model_path):
        """Load trained model weights"""
        try:
            checkpoint = torch.load(model_path, map_location=self.device, weights_only=False)
            self.model.load_state_dict(checkpoint['model_state_dict'])
            self.model.to(self.device)
            self.model.eval()
            print(f"Model loaded successfully from {model_path}")
            print(f"Trained AUC: {checkpoint.get('val_auc', 'N/A')}")
        except Exception as e:
            print(f"Error loading model: {e}")

    def preprocess_image(self, image_path):
        """
        Preprocess retina fundus image
        Includes CLAHE enhancement (same as training)
        """
        # Read image
        img = cv2.imread(image_path)
        if img is None:
            raise ValueError(f"Cannot read image from {image_path}")

        # Convert to RGB
        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

        # Apply CLAHE enhancement (same as training)
        lab = cv2.cvtColor(img, cv2.COLOR_RGB2LAB)
        l, a, b = cv2.split(lab)
        clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8, 8))
        l_clahe = clahe.apply(l)
        lab_clahe = cv2.merge([l_clahe, a, b])
        img_enhanced = cv2.cvtColor(lab_clahe, cv2.COLOR_LAB2RGB)

        #Convert to PIL Image
        img_pil = Image.fromarray(img_enhanced)
        #img_pil = Image.fromarray(img)

        # Apply transformations
        img_tensor = self.transform(img_pil)

        return img_tensor, img, img_enhanced
        #return img_tensor, img, img

    def predict(self, image_path):
        """
        Make prediction on a single retina fundus image
        """
        # Preprocess image
        img_tensor, original_img, enhanced_img = self.preprocess_image(image_path)

        # Add batch dimension
        img_tensor = img_tensor.unsqueeze(0).to(self.device)

        # Make prediction
        with torch.no_grad():
            output = self.model(img_tensor)
            probability = torch.sigmoid(output).cpu().numpy()[0][0]

        # Interpretation
        prediction = 1 if probability >= 0.75 else 0
        confidence = probability if prediction == 1 else (1 - probability)

        return {
            'probability': probability,
            'prediction': prediction,
            'confidence': confidence,
            'original_image': original_img,
            'enhanced_image': enhanced_img,
            'risk_level': self._interpret_risk(probability)
        }

    def _interpret_risk(self, probability):
        """Interpret the risk based on probability"""
        if probability < 0.4:
            return "Low Risk"
        elif probability < 0.75:
            return "Moderate Risk"
        else:
            return "High Risk"

