#custom dataset for fundus image with hypertension labels
class FundusDataset(Dataset):

  def __init__(self, dataframe, img_dir, transform=None, use_clahe=True, use_green_channel=False):
    self.df = dataframe.reset_index(drop=True)
    self.img_dir = img_dir
    self.transform = transform
    self.use_clahe = use_clahe
    self.use_green_channel = use_green_channel

    print(f"Data Initialized")
    print(f"Total Samples: {len(self.df)}")
    print(f"HTR distribution: {dict(self.df['disease'].value_counts())}")
    print(f" CLAHE Enhancement: {'Enabled' if use_clahe else 'Disable'}")
    print(f" Green channel only: {'Yes' if use_green_channel else 'No'}")


  def __len__(self):
    return len(self.df)

#applying clahe to enhance vessel visibility
  def apply_clahe(self, img):
    lab = cv2.cvtColor(img, cv2.COLOR_RGB2LAB)
    l, a, b = cv2.split(lab)
    clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8,8))
    l_clahe = clahe.apply(l)
    lab_clahe = cv2.merge([l_clahe, a, b])
    img_clahe = cv2.cvtColor(lab_clahe, cv2.COLOR_LAB2RGB)

    return img_clahe


  def __getitem__(self, idx):
    row = self.df.iloc[idx]
    img_name = row['images']
    disease = row['disease']

    #binary label mapping
    # N = 0 (healthy),  H = 1 (hypertensive)
    label = 1 if disease == 'H' else 0

    img_path = os.path.join(self.img_dir, img_name)

    #load image
    img = cv2.imread(img_path)
    if img is None:
      raise ValueError(f"Failed to load image: {img_path}")

    img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

    if self.use_clahe:
      img = self.apply_clahe(img)

    if self.use_green_channel:
      img = img[:, :, 1]
      img = cv2.cvtColor(img, cv2.COLOR_GRAY2RGB)

    img = Image.fromarray(img)

    if self.transform:
      img = self.transform(img)

    return img, torch.tensor(label, dtype=torch.float32), img_name


#get image transform for training and validation
def get_transforms(img_size=224, augment=True, augmentation_type='default'):
  if not augment:
    return transforms.Compose([
        transforms.Resize((img_size, img_size)),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
    ])

  if augmentation_type == 'normal':
    return transforms.Compose([
        transforms.Resize((img_size, img_size)),
        transforms.RandomRotation(5),
        transforms.RandomHorizontalFlip(p=0.3),
        transforms.ColorJitter(brightness=0.05, contrast=0.05, saturation=0.05),
        transforms.RandomAffine(degrees=0, translate=(0.02, 0.02), scale=(0.98, 1.02)),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
    ])
  elif augmentation_type == 'hypertensive':
    return transforms.Compose([
        transforms.Resize((img_size, img_size)),
        transforms.RandomRotation(15),
        transforms.RandomHorizontalFlip(p=0.5),
        transforms.ColorJitter(brightness=0.15, contrast=0.15, saturation=0.15),
        transforms.RandomAffine(degrees=0, translate=(0.08, 0.08), scale=(0.9, 1.1)),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
    ])

  else:
    return transforms.Compose([
        transforms.Resize((img_size, img_size)),
        transforms.RandomRotation(10),
        transforms.RandomHorizontalFlip(p=0.5),
        transforms.ColorJitter(brightness=0.1, contrast=0.1, saturation=0.1),
        transforms.RandomAffine(degrees=0, translate=(0.05, 0.05), scale=(0.95, 1.05)),
        transforms.ToTensor(),
        transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
    ])

