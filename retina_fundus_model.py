"""# **Model Architecture**"""

#transfer learning for hypertension prediction

class FundusHypertensionModel(nn.Module):
  def __init__(self, backbone='resnet50', pretrained=True, feature_dim=512, dropout=0.6):
    super().__init__()

    print(f"Building Imbalanced model with {backbone.upper()} backbone")

    if backbone == 'resnet50':
      self.backbone = models.resnet50(pretrained=pretrained)
      in_features = self.backbone.fc.in_features
      self.backbone.fc = nn.Identity()

    elif backbone == 'efficientnet_b3':
      self.backbone = models.efficientnet_b3(pretrained=pretrained)
      in_features = self.backbone.classifier[1].in_features
      self.backbone.classifier = nn.Identity()

    elif backbone == 'densenet121':
      self.backbone = models.densenet121(pretrained=pretrained)
      in_features = self.backbone.classifier.in_features
      self.backbone.classifier = nn.Identity()
    else:
      raise ValueError(f"Unsupported backbone: {backbone}")

    print(f" Backbone output features: {in_features}")
    print(f" Custom feature dimension: {feature_dim}")
    print(f" Dropout rate: {dropout} (inceased for regularization)")
    print(f" Pretrained weights: {'Loaded' if pretrained else 'Random initialization'}")


    #enhanced feature extractor with more regularization
    self.feature_extractor = nn.Sequential(
        nn.Linear(in_features, feature_dim),
        nn.BatchNorm1d(feature_dim),
        nn.ReLU(),
        nn.Dropout(dropout),


        #additional layer for better representation
        nn.Linear(feature_dim, feature_dim // 2),
        nn.BatchNorm1d(feature_dim // 2),
        nn.ReLU(),
        nn.Dropout(dropout * 0.8)


    )

    #binary classifier (logits)
    # self.classifier = nn.Sequential(
    #     nn.Linear(feature_dim // 2, 1),
    #     nn.Sigmoid()
    # )
    # self.feature_dim = feature_dim // 2

    self.classifier = nn.Linear(feature_dim // 2, 1)

    self.feature_dim = feature_dim // 2


  def forward(self, x, return_features=False):
    x = self.backbone(x)
    features = self.feature_extractor(x)
    logits = self.classifier(features)

    if return_features:
      return features, logits
    else:
      return logits

