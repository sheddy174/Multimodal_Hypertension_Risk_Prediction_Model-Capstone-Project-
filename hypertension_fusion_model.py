class RetinaHypertensionPredictor:
    def __init__(self, model_path=None):
        self.device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
        print(f"Using device: {self.device}")
        self.model = self._create_model()
        if model_path and os.path.exists(model_path):
            self.load_model(model_path)
        else:
            print("Warning: Model path not provided or not found. Using randomly initialized model.")

        self.transform = transforms.Compose([
            transforms.Resize((224, 224)),
            transforms.ToTensor(),
            transforms.Normalize(mean=[0.485, 0.456, 0.406],
                                 std=[0.229, 0.224, 0.225])
        ])

    def _create_model(self):
        class FundusHypertensionModel(nn.Module):
            def __init__(self):
                super().__init__()
                self.backbone = models.resnet50(pretrained=False)
                in_features = self.backbone.fc.in_features
                self.backbone.fc = nn.Identity()
                self.feature_extractor = nn.Sequential(
                    nn.Linear(in_features, 512),
                    nn.BatchNorm1d(512),
                    nn.ReLU(),
                    nn.Dropout(0.5),
                    nn.Linear(512, 256),
                    nn.BatchNorm1d(256),
                    nn.ReLU(),
                    nn.Dropout(0.4)
                )
                self.classifier = nn.Linear(256, 1)

            def forward(self, x):
                x = self.backbone(x)
                features = self.feature_extractor(x)
                logits = self.classifier(features)
                return logits

        return FundusHypertensionModel().to(self.device)

    def load_model(self, model_path):
        try:
            checkpoint = torch.load(model_path, map_location=self.device, weights_only=False)
            self.model.load_state_dict(checkpoint['model_state_dict'])
            self.model.to(self.device)
            self.model.eval()
            print(f"Model loaded successfully from {model_path}")
            print(f"Trained AUC: {checkpoint.get('val_auc', 'N/A')}")
        except Exception as e:
            print(f"Error loading model: {e}")

