class SamplePrediction:
    def __init__(self, clinical_model, fundus_model, fusion_model, preprocessor, device='cpu'):
        self.clinical_model = clinical_model
        self.fundus_model   = fundus_model
        self.fusion_model   = fusion_model
        self.preprocessor   = preprocessor
        self.device         = device

        # Set fundus model to evaluation mode
        self.fundus_model.eval()

        # Image transformations (must match training)
        self.image_transform = transforms.Compose([
            transforms.Resize((224, 224)),
            transforms.ToTensor(),
            transforms.Normalize(
                mean=[0.485, 0.456, 0.406],
                std=[0.229, 0.224, 0.225]
            )
        ])

    def preprocess_image(self, image_path):
        """CLAHE preprocessing — must match fundus model training"""
        img = cv2.imread(image_path)
        if img is None:
            raise ValueError(f"Cannot read image from {image_path}")

        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)

        # Apply CLAHE (same as training)
        lab = cv2.cvtColor(img, cv2.COLOR_RGB2LAB)
        l, a, b = cv2.split(lab)
        clahe   = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8, 8))
        l_clahe = clahe.apply(l)
        lab_clahe   = cv2.merge([l_clahe, a, b])
        img_enhanced = cv2.cvtColor(lab_clahe, cv2.COLOR_LAB2RGB)

        img_pil    = Image.fromarray(img_enhanced)
        img_tensor = self.image_transform(img_pil)

        return img_tensor, img, img_enhanced

    def predict(self, clinical_data, retina_image_path, verbose=True):
        # Clinical prediction
        if isinstance(clinical_data, dict):
            clinical_data = pd.DataFrame([clinical_data])

        input_features = [
            'sex', 'age', 'cigsPerDay', 'totChol', 'sysBP', 'diaBP',
            'BMI', 'heartRate', 'glucose', 'male', 'currentSmoker',
            'BPMeds', 'diabetes'
        ]

        clinical_data = clinical_data[input_features]
        clinical_processed = self.preprocessor.transform(clinical_data)
        clinical_prob = self.clinical_model.predict_proba(clinical_processed)[0, 1]
        print("Clinical probability:", clinical_prob)


        #  Fundus prediction (CLAHE + sigmoid)
        img_tensor, original_img, enhanced_img = self.preprocess_image(retina_image_path)
        img_tensor = img_tensor.unsqueeze(0).to(self.device)

        with torch.no_grad():
            output = self.fundus_model(img_tensor)
            fundus_prob = torch.sigmoid(output).cpu().numpy()[0][0]  # sigmoid applied here
            print("Fundus probability:", fundus_prob)


         # Weights derived from AUC
        weights = [clinical_prob, fundus_prob]
        models  = ['Clinical-Lifestyle', 'Retina Fundus']

        # Bar plot
        plt.figure(figsize=(6,4))
        bars = plt.bar(models, weights, color=['skyblue','salmon'])
        plt.ylim(0,1)
        plt.ylabel('Weight')
        plt.title('Performance-Based Model Weights (from AUC)')

        # Annotate the bars with weight values
        for bar, w in zip(bars, weights):
            plt.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.02, f"{w:.2f}",
                    ha='center', va='bottom', fontsize=10)

        plt.show()

        #  Fusion prediction
        fusion_prob   = self.fusion_model.predict_proba(
            np.array([clinical_prob]),
            np.array([fundus_prob])
        )[0]
        print("Fusion probability is..",fusion_prob)

        risk_category = self.fusion_model.get_risk_category(fusion_prob)

        #Recommendation
        if risk_category == "Low Risk":
            recommendation = "Continue regular health monitoring"
        elif risk_category == "Medium Risk":
            recommendation = "Recommend lifestyle modifications and closer monitoring"
        else:
            recommendation = "Recommend immediate medical consultation and intervention"

        results = {
            'clinical_probability': float(clinical_prob),
            'fundus_probability'  : float(fundus_prob),
            'fusion_probability'  : float(fusion_prob),
            'risk_category'       : risk_category,
            'recommendation'      : recommendation,
            'clinical_prediction' : int(clinical_prob >= 0.5),
            'fundus_prediction'   : int(fundus_prob >= 0.75),  # matches retina model threshold
            'fusion_prediction'   : int(fusion_prob >= 0.5),
            'original_image'      : original_img,
            'enhanced_image'      : enhanced_img
        }

        if verbose:
            self._display_results(results, clinical_data.iloc[0], retina_image_path)

        return results

