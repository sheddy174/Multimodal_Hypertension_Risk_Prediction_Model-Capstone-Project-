def evaluate_fusion_model(fusion_model, clinical_probs, image_probs, y_true, model_name="Fusion Model"):

    # Ensure proper shape
    y_true = y_true.flatten()

    # Get probabilities
    y_proba = fusion_model.predict_proba(clinical_probs, image_probs).flatten()

    # Optional: Optimal threshold (better for research)
    fpr, tpr, thresholds = roc_curve(y_true, y_proba)
    optimal_idx = np.argmax(tpr - fpr)
    optimal_threshold = thresholds[optimal_idx]

    # Predictions
    y_pred = (y_proba >= optimal_threshold).astype(int)

    # Metrics
    metrics = {
        'Accuracy': accuracy_score(y_true, y_pred),
        'Precision': precision_score(y_true, y_pred, zero_division=0),
        'Recall': recall_score(y_true, y_pred, zero_division=0),
        'F1-Score': f1_score(y_true, y_pred, zero_division=0),
        'ROC-AUC': roc_auc_score(y_true, y_proba)
    }

    # Print results
    print(f"{model_name} - Performance Metrics")
    print(f"Optimal Threshold: {optimal_threshold:.4f}\n")

    for metric, value in metrics.items():
        print(f"{metric:15s}: {value:.4f} ({value*100:.2f}%)")
    print()

    return metrics, y_pred, y_proba


